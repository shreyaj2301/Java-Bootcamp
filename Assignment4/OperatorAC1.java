class OperatorAC1{
    public static void main(String[]args)
    {
         System.out.println((5-2)==(6-3));//true
         System.out.println((5+2)==(10-5));//false
         System.out.println((12/6)==(1+1));//true
         System.out.println((5*2)==(100/10));//true
         System.out.println((5%2)==(100/10));//false 5%2is 1 is not equal to 100/10is 10
         
    } 
}