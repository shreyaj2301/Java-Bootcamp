class OperatorCB3{
    public static void main(String[]args)
    { 
        //Program using a camparision and boolean NOT operator
      //   A       O/P      
      ///True    False    
      // False   True   
      //we have to use  a not operator before the expression
       System.out.println(!(5==5));//false
       System.out.println(!(5>=2));//false
       System.out.println(!(12>2));//false
       System.out.println(!(5<=2));//true
       System.out.println(!(5<2));//true

    } 
}