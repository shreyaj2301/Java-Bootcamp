class Typescriptfloat{
    public static void main(String[]args)
    {  
        // float  showing as int, short,byte,double,char,long,boolean
        float a = 60.25f;
        float b = 23.5f;
        System.out.println("float a and b as int -" +(int)(a+b)); // print as pretty printing
        System.out.println("float a and bas short -"+(short) (a-b));
        System.out.println("float a and b long -"+(long) (a*b));
        System.out.println("float a and b byte -"+(float) (a%b));
        System.out.println("float a and b double -"+(double) (a/b));
        System.out.println("float a and b char  -" +(char)(a+b));// it showing S
        //System.out.println("float a in boolean -"+(boolean) a);

    }
}