 class Assimulti{
    public static void main(String[] args) {
        int a = 5;
        int b =  10;
         int c  =  15;
        a *= 3; // equal to a = a * 3
        b *= 5; // equal to b = b * 5
        c *= 10; // equal to c = c * 10

        System.out.println("a after Multiplication: " + a);
         System.out.println("b after Multiplication: " + b);
        System.out.println("c after Multiplication: " + c);
    }
}