// Reverse an Array: Write a program to reverse the elements of an array in place.

class Question4{

    public static void main (String[] args){

        int [] arr3 = { 91 , 92 , 93 , 94 ,95};

        for(int i = 5 - 1; i >= 0 ; i--){
            System.out.print(arr3[i] + " ");
        }
        System.out.println();
    }
}