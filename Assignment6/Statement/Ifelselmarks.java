 class Ifelselmarks {
    public static void main(String[] args) {
        int marks = 75;

        if (marks >= 90) {
            System.out.println("Rank: A");
        } else if (marks >= 80) {
            System.out.println("Rank: B");
        } else if (marks >= 70) {
            System.out.println("Rank: C");
        } else if (marks >= 60) {
            System.out.println("Rank: D");
        } else {
            System.out.println("Rank: F");
        }
    }
}
