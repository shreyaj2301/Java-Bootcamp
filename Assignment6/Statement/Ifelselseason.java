 class Ifelselseason {
    public static void main(String[] args) {
        int month = 7;

        if (month >= 3 && month <= 5) {
            System.out.println("Summer");
        } else if (month >= 6 && month <= 8) {
            System.out.println("monsoon");
        }  else {
            System.out.println("Winter");
        }
    }
}