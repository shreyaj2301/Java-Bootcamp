class Datatype6{
    public static void main(String[]args){
        char ch1 = 'a'; 
        char ch2 = '5';

        System.out.println(ch1 + ch2);
        System.out.println(ch1 - ch2);
        System.out.println(ch1 * ch2);
        System.out.println(ch1 / ch2);
        System.out.println(ch1 % ch2);
    }
}