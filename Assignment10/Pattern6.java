class Pattern6{

    public static void main (String [] args){

        for(int i = 0; i < 7; i++){
            for(int j = 0; j < 7 - i; j++){
                System.out.print(" * ");
            }
            System.out.println();
        }
            for(int a = 0; a < 7; a++){
            for(int k = 0; k < a + 1; k++){
                System.out.print(" * ");
            }
            System.out.println();
        
    }
    }
}